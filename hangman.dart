Hangman Game Project

The game will randomly select a word from a predefined list, and the player will have to guess the word by suggesting letters. For each incorrect guess, a part of a hangman figure will be drawn. The game will end when the player guesses the word correctly or the hangman figure is complete.

How to play:

    Run the code in your terminal.
    The game will randomly select a word and you will see a series of underscores representing the word.
    Guess a letter by typing it in and pressing enter.
    If you guess a letter correctly, it will be filled in in the correct position.
    If you guess incorrectly, a part of the hangman figure will be drawn.
    Keep guessing until you correctly guess the word or the hangman figure is complete.

I hope you enjoy playing Hangman! 
        




           












                   
